package demoproject;
import java.util.*;
public class PrintingN_NaturalNumber {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter the value of n:");
        int n=sc.nextInt();
        System.out.println("The values are:");
        for(int i=0;i<=n;i++)
        {
            System.out.println(i);
            
        }
             
        
    }
    
}
